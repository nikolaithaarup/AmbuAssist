function normalizeText(value: string): string {
  return value.trim().toLowerCase().replace(/\s+/g, " ");
}

export function normalizeBydel(raw: string): Bydel | null {
  return BYDEL_ALIASES[normalizeText(raw)] ?? null;
}

/**
 * Use this for imported/raw street rows from the PDF.
 */
export function normalizeStreetRow(
  row: Omit<StreetRow, "bydel"> & { bydel: string },
): StreetRow | null {
  const normalizedBydel = normalizeBydel(row.bydel);
  if (!normalizedBydel) return null;

  return {
    street: row.street.trim(),
    bydel: normalizedBydel,
  };
}

/**
 * Main lookup helper.
 */
export function getByenHospitalForStreet(
  streetRow: StreetRow,
  category: ByenCategory,
): HospitalCode {
  return BYEN_MAP[streetRow.bydel][category];
}

/**
 * Helpful if user typed a street manually.
 */
export function findStreetByName(
  query: string,
  streets: StreetRow[],
): StreetRow | undefined {
  const normalizedQuery = normalizeText(query);
  return streets.find((row) => normalizeText(row.street) === normalizedQuery);
}

